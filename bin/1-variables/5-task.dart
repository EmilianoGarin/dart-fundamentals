void main () {
  int a = 10;
  int b = 3;

  print("a + b = " + (a + b).toString());
  print("a - b = " + (a - b).toString());
  print("a * b = " + (a * b).toString());
  print("a / b = " + (a / b).toString());
  print("a > b = " + (a > b).toString());
  print("a == b = " + (a == b).toString());
  print("a > 5 && b < 5 = " + (a > 5 && b < 5).toString());
  print("a < 5 || b < 5 = " + (a < 5 || b < 5).toString());
}
